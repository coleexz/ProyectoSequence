    @Override
    public void actionPerformed(ActionEvent e) {
        if(e.getSource().equals(CREAR)){
            if(jPasswordField1.getText().isEmpty()|| jTextField1.getText().isEmpty()){
                JOptionPane.showMessageDialog(this,"no ha rellenado todos los datos"); 
            }else {
                String usr = jTextField1.getText();
                String pass = jPasswordField1.getText();
                
                if(Player.buscarUser(usr)==null){
                    Player.addUser(usr, pass, "");
                    Player.saveUsersToFile("Users/users.usr");
                    JOptionPane.showMessageDialog(this,"Usuario registrado correctamente");
                } else {
                    JOptionPane.showMessageDialog(this,"Ya hay un usuario registrado con ese nombre");
                }
            }
            jTextField1.setText("");
            jPasswordField1.setText("");
        } else if(e.getSource().equals(LOGIN)){
           LOGIN login;
            try {
                login = new LOGIN();
                login.setVisible(true);
                this.dispose();
            } catch (IOException ex) {
                Logger.getLogger(CREATE.class.getName()).log(Level.SEVERE, null, ex);
            }
           
        }
    }
    
